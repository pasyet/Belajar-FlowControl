public class JavaForIf{
    public static void main(String[] args){
        for(int i=0;i<10;i++){//kondisi awal i =0; i akan incrent i=0+1; i akan dicek apa masih kurang dari 10, lalu ke if selanjutnya
            if(i==5){//jika i sama 5 maka dicetak output akan dieksekusi
                System.out.println("Sekarang ada diposis ke-5");
            }
        }
    }
}