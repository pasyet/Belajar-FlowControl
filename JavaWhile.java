public class JavaWhile{
    public static void main (String[] args){
        int i = 0; //i first input 0 
        while(i<5){ //i akan diulangan sebanyak kurang dari 5 
            System.out.println("Saya berulang sebanyak "+i); //output akan mencetak selama i masih kurang dari 5
            i++; //i akan di increment 
        }
    }
}