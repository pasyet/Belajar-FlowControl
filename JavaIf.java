public class JavaIf {
    public static void main (String[] args){
        Integer bil = 10; //bil integer yang input 10
        if (bil == 100){ //jika bil sama dengan 100 (true) mengekusi Bilangan sama
            System.out.println ("Bilangan Sama");
        }
        else {
            System.out.println ("Bilangan Tidak Sama"); //dieskusi jika (false) 
        }
    }
}