public class JavaWhileDo{
    public static void main (String[] args){
        int i = 0; //i first input 0
        do { // akan mencetak output 
            System.out.println("Saya berulang sebanyak " +i);
            i++; // increant
        } while (i<10); // i akan di lakukan perulangan kurang dari 10
    }

}