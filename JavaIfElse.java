public class JavaIfElse{
    public static void main (String[] args) {
        String word = "Hey";//input string word hey
        if(word.equals("hey")){ //jika word comparing dengan hey maka
            System.out.println("data sama"); //output mencetak data sama (true)
        }
        else{ //jika false maka
            System.out.println("data beda!"); //output mencetak data beda (false)
        }
    }
}